/// Registration method recorded for an auth.md agent identity.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum RegistrationType {
    IdentityAssertion,
    ServiceAuth,
    Anonymous,
}

impl RegistrationType {
    pub(crate) fn as_str(self) -> &'static str {
        match self {
            RegistrationType::IdentityAssertion => "identity_assertion",
            RegistrationType::ServiceAuth => "service_auth",
            RegistrationType::Anonymous => "anonymous",
        }
    }

    fn from_string(value: String) -> Self {
        match value.as_str() {
            "identity_assertion" => RegistrationType::IdentityAssertion,
            "service_auth" => RegistrationType::ServiceAuth,
            "anonymous" => RegistrationType::Anonymous,
            _ => RegistrationType::Anonymous,
        }
    }
}

/// Durable lifecycle status for an agent identity.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum AgentIdentityStatus {
    Active,
    Claimed,
    Revoked,
}

impl AgentIdentityStatus {
    pub(crate) fn as_str(self) -> &'static str {
        match self {
            AgentIdentityStatus::Active => "active",
            AgentIdentityStatus::Claimed => "claimed",
            AgentIdentityStatus::Revoked => "revoked",
        }
    }

    fn from_string(value: String) -> Self {
        match value.as_str() {
            "active" => AgentIdentityStatus::Active,
            "claimed" => AgentIdentityStatus::Claimed,
            "revoked" => AgentIdentityStatus::Revoked,
            _ => AgentIdentityStatus::Revoked,
        }
    }
}

/// Durable lifecycle status for a user-code claim ceremony attempt.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum ClaimAttemptStatus {
    Pending,
    Completed,
    Expired,
}

impl ClaimAttemptStatus {
    pub(crate) fn as_str(self) -> &'static str {
        match self {
            ClaimAttemptStatus::Pending => "pending",
            ClaimAttemptStatus::Completed => "completed",
            ClaimAttemptStatus::Expired => "expired",
        }
    }

    fn from_string(value: String) -> Self {
        match value.as_str() {
            "pending" => ClaimAttemptStatus::Pending,
            "completed" => ClaimAttemptStatus::Completed,
            "expired" => ClaimAttemptStatus::Expired,
            _ => ClaimAttemptStatus::Expired,
        }
    }
}

/// Full agent identity row. Scope fields are stored as JSON array strings; route code owns
/// parsing/validation because it knows the applicable grant flow and permission grammar.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct AgentIdentityRow {
    pub(crate) id: String,
    /// The owning account DID, or `None` for an unclaimed `anonymous` registration (V038): an
    /// anonymous identity has no owning account until a later claim ceremony binds one.
    pub(crate) did: Option<String>,
    pub(crate) registration_type: RegistrationType,
    pub(crate) issuer: Option<String>,
    pub(crate) subject: Option<String>,
    pub(crate) email: Option<String>,
    pub(crate) scopes: String,
    pub(crate) identity_assertion: Option<String>,
    pub(crate) assertion_expires_at: String,
    pub(crate) pre_claim_scopes: Option<String>,
    pub(crate) claim_token: Option<String>,
    pub(crate) claim_token_expires_at: Option<String>,
    pub(crate) status: AgentIdentityStatus,
    pub(crate) created_at: String,
    pub(crate) updated_at: String,
}

/// Fields needed to insert an agent identity. IDs/tokens/assertions are generated by callers.
pub(crate) struct NewAgentIdentity<'a> {
    pub(crate) id: &'a str,
    /// The owning account DID, or `None` for an unclaimed `anonymous` registration (V038).
    pub(crate) did: Option<&'a str>,
    pub(crate) registration_type: RegistrationType,
    pub(crate) issuer: Option<&'a str>,
    pub(crate) subject: Option<&'a str>,
    pub(crate) email: Option<&'a str>,
    pub(crate) scopes: &'a str,
    pub(crate) identity_assertion: Option<&'a str>,
    pub(crate) assertion_expires_at: &'a str,
    pub(crate) pre_claim_scopes: Option<&'a str>,
    pub(crate) claim_token: Option<&'a str>,
    pub(crate) claim_token_expires_at: Option<&'a str>,
}

/// Result of inserting an agent identity.
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub(crate) enum InsertAgentIdentityOutcome {
    Created,
    /// Primary-key or `(issuer, subject)` uniqueness collision.
    Duplicate,
}

/// Claim ceremony row with expiry derived at query time.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(crate) struct AgentClaimAttemptRow {
    pub(crate) id: String,
    pub(crate) identity_id: String,
    pub(crate) user_code: String,
    pub(crate) user_code_expires_at: String,
    pub(crate) email: Option<String>,
    pub(crate) status: ClaimAttemptStatus,
    pub(crate) created_at: String,
    /// Derived: `datetime(user_code_expires_at) <= datetime('now')` at query time.
    pub(crate) is_expired: bool,
}

impl AgentClaimAttemptRow {
    /// A claim attempt can be completed only while pending and unexpired.
    pub(crate) fn is_pending(&self) -> bool {
        self.status == ClaimAttemptStatus::Pending && !self.is_expired
    }
}

/// Fields needed to insert a user-code claim attempt.
pub(crate) struct NewAgentClaimAttempt<'a> {
    pub(crate) id: &'a str,
    pub(crate) identity_id: &'a str,
    pub(crate) user_code: &'a str,
    pub(crate) user_code_expires_at: &'a str,
    pub(crate) email: Option<&'a str>,
}

/// Insert a new agent identity row. Generic over the executor so future route code can
/// compose registration and claim-attempt writes inside one transaction.
pub(crate) async fn insert_agent_identity<'e, E>(
    executor: E,
    identity: &NewAgentIdentity<'_>,
) -> Result<InsertAgentIdentityOutcome, ApiError>
where
    E: sqlx::Executor<'e, Database = Sqlite>,
{
    let result = sqlx::query(
        "INSERT INTO agent_identities \
         (id, did, registration_type, issuer, subject, email, scopes, identity_assertion, \
          assertion_expires_at, pre_claim_scopes, claim_token, claim_token_expires_at, \
          created_at, updated_at) \
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, datetime('now'), datetime('now'))",
    )
    .bind(identity.id)
    .bind(identity.did)
    .bind(identity.registration_type.as_str())
    .bind(identity.issuer)
    .bind(identity.subject)
    .bind(identity.email)
    .bind(identity.scopes)
    .bind(identity.identity_assertion)
    .bind(identity.assertion_expires_at)
    .bind(identity.pre_claim_scopes)
    .bind(identity.claim_token)
    .bind(identity.claim_token_expires_at)
    .execute(executor)
    .await;

    match result {
        Ok(_) => Ok(InsertAgentIdentityOutcome::Created),
        Err(e) if is_unique_violation(&e) => Ok(InsertAgentIdentityOutcome::Duplicate),
        Err(e) => {
            tracing::error!(identity_id = %identity.id, error = %e, "DB error inserting agent identity");
            Err(ApiError::new(
                ErrorCode::InternalError,
                "failed to create agent identity",
            ))
        }
    }
}

/// Fetch an agent identity by registration id.
pub(crate) async fn get_agent_identity(
    db: &sqlx::SqlitePool,
    id: &str,
) -> Result<Option<AgentIdentityRow>, ApiError> {
    fetch_identity_by(db, "id", id).await
}

/// Fetch an agent identity by one-time claim token.
pub(crate) async fn get_agent_identity_by_claim_token(
    db: &sqlx::SqlitePool,
    claim_token: &str,
) -> Result<Option<AgentIdentityRow>, ApiError> {
    fetch_identity_by(db, "claim_token", claim_token).await
}

/// Fetch an agent identity by its `(issuer, subject)` pair.
///
/// Backs the `identity_assertion` flow: an ID-JAG's `(iss, sub)` is the stable key for an agent's
/// registration, deduplicated by the partial unique index `idx_agent_identities_iss_sub`.
pub(crate) async fn get_agent_identity_by_issuer_subject(
    db: &sqlx::SqlitePool,
    issuer: &str,
    subject: &str,
) -> Result<Option<AgentIdentityRow>, ApiError> {
    let row = sqlx::query_as::<_, IdentitySqlRow>(
        "SELECT id, did, registration_type, issuer, subject, email, scopes, identity_assertion, \
                assertion_expires_at, pre_claim_scopes, claim_token, claim_token_expires_at, \
                status, created_at, updated_at \
         FROM agent_identities WHERE issuer = ? AND subject = ?",
    )
    .bind(issuer)
    .bind(subject)
    .fetch_optional(db)
    .await
    .map_err(|e| {
        tracing::error!(error = %e, "DB error fetching agent identity by issuer/subject");
        ApiError::new(ErrorCode::InternalError, "failed to load agent identity")
    })?;
    Ok(row.map(into_identity_row))
}

async fn fetch_identity_by(
    db: &sqlx::SqlitePool,
    column: &str,
    value: &str,
) -> Result<Option<AgentIdentityRow>, ApiError> {
    let sql = match column {
        "id" => {
            "SELECT id, did, registration_type, issuer, subject, email, scopes, identity_assertion, \
                    assertion_expires_at, pre_claim_scopes, claim_token, claim_token_expires_at, \
                    status, created_at, updated_at \
             FROM agent_identities WHERE id = ?"
        }
        "claim_token" => {
            "SELECT id, did, registration_type, issuer, subject, email, scopes, identity_assertion, \
                    assertion_expires_at, pre_claim_scopes, claim_token, claim_token_expires_at, \
                    status, created_at, updated_at \
             FROM agent_identities WHERE claim_token = ?"
        }
        _ => unreachable!("unsupported agent identity lookup column"),
    };

    let row = sqlx::query_as::<_, IdentitySqlRow>(sql)
    .bind(value)
    .fetch_optional(db)
    .await
    .map_err(|e| {
        tracing::error!(lookup_column = %column, error = %e, "DB error fetching agent identity");
        ApiError::new(ErrorCode::InternalError, "failed to load agent identity")
    })?;

    Ok(row.map(into_identity_row))
}

/// Update identity status. Used by claim completion and revocation flows.
pub(crate) async fn set_agent_identity_status<'e, E>(
    executor: E,
    id: &str,
    status: AgentIdentityStatus,
) -> Result<bool, ApiError>
where
    E: sqlx::Executor<'e, Database = Sqlite>,
{
    let result = sqlx::query(
        "UPDATE agent_identities SET status = ?, updated_at = datetime('now') WHERE id = ?",
    )
    .bind(status.as_str())
    .bind(id)
    .execute(executor)
    .await
    .map_err(|e| {
        tracing::error!(identity_id = %id, error = %e, "DB error updating agent identity status");
        ApiError::new(ErrorCode::InternalError, "failed to update agent identity")
    })?;
    Ok(result.rows_affected() == 1)
}

